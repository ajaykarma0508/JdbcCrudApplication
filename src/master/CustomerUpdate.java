    private void idActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_idActionPerformed
         String id1=id.getText();
        idno=Integer.parseInt(id1);
        
        try{
            Connection con=InitConnection.initConn();
            Statement stm1=con.createStatement();
            ResultSet rs=stm1.executeQuery("select * from customer where c_id ="+idno);
            if(rs.next())
            {
                cn.setText(rs.getString(2));
                cm.setText(rs.getInt(3)+"");
                ca.setText(rs.getString(4));
                cc.setText(rs.getString(5));
            }
        }
        catch(Exception e)
        {
            System.out.println(e);
        } 
    }//GEN-LAST:event_idActionPerformed

