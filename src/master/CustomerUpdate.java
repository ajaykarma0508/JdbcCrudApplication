    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
       String custnm=cn.getText();
       String cadd= ca.getText();
       String city1=cc.getText();
       String mob1=cm.getText();
       
       try{
           Connection con=InitConnection.initConn();
           PreparedStatement ps=con.prepareStatement("update customer set c_name=?,cadd=?,c_city=?,c_mob=? where c_id="+idno);
           ps.setString(1,custnm);
           ps.setString(2,cadd);
           ps.setString(3,city1);
           ps.setString(4,mob1);
           ps.executeUpdate();
           JOptionPane.showMessageDialog(null," Data updated Successfully");
           setVisible(false);
           new CustomerUpdate().setVisible(true);
        
           
       }
       catch(Exception e)
       {
           JOptionPane.showMessageDialog(null,"connection error");
       }
    }//GEN-LAST:event_updateActionPerformed

