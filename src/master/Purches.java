    public void getData()
    {
        try
        {
            con=InitConnection.initConn();
            Statement stm=con.createStatement();
            

// ...............................invoice no. ..........................................................
        
        ResultSet rs1=stm.executeQuery("Select invno from purches");
        if(rs1.next())
        {
            inv=rs1.getInt(1);
            while(rs1.next())
            {
                inv=rs1.getInt(1);
            }
            inv=inv+1;
        }
        else{
            inv=101;
        }
        invn.setText(inv+"");

///////////////////////////////////////  for Customer   //////////////////////////////////////////

            ResultSet rs2=stm.executeQuery("select c_id,c_name,c_city from customer");
            v1=new Vector();
            v2=new Vector();
            
            
            while(rs2.next())
            {
                v1.addElement(rs2.getInt(1));
                cb1.addItem(rs2.getString(2));
                v2.addElement(rs2.getString(3));
            }
            
/////////////////////////////////// for Product   ////////////////////////////////////////////
            
            ResultSet rs3 = stm.executeQuery("select p_id,p_name,pur_price,stock from product");
            v3=new Vector();
            v4=new Vector();
            v5=new Vector();
          
          
            
            while(rs3.next())
            {
                v3.addElement(rs3.getInt(1));
                cb2.addItem(rs3.getString(2));
                v4.addElement(rs3.getInt(3));
                v5.addElement(rs3.getInt(4));
                
            }
        }
        
        catch(Exception e)
        {
            System.out.print(e);
        }
        
    }

