    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
      try
      {
         // .........................................................................
          
          PreparedStatement ps1=con.prepareStatement("update  product set stock=? where p_id="+pid1);
          ps1.setInt(1,Integer.parseInt(stock.getText()));
          ps1.executeUpdate();
          
         PreparedStatement ps=con.prepareStatement("insert into purches (invno,c_id,p_id,pur_price,qty,total) values(?,?,?,?,?,?)");
         ps.setInt(1,Integer.parseInt(invn.getText()));
         ps.setInt(2,cid1);
         ps.setInt(3,pid1);
         ps.setInt(4,Integer.parseInt(pp.getText()));
         ps.setInt(5,Integer.parseInt(pq.getText()));
         ps.setInt(6,Integer.parseInt(ta.getText()));
         //ps.executeUpdate();
         int x=ps.executeUpdate();
         JOptionPane.showMessageDialog(null," Data inserted Successfully");
         setVisible(false);
         new Purches().setVisible(true);
         con.close();
         this.getData();

         
      }
      catch(Exception e)
      {
          System.out.println(e);
      }
    }//GEN-LAST:event_submitActionPerformed

